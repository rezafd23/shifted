package day8.tugas2;//package day8.tugas2;

public class Mahasiswa {
    private String nama;
    private int fisika;
    private int biologi;
    private int kimia;

    public Mahasiswa() {
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getFisika() {
        return fisika;
    }

    public void setFisika(int fisika) {
        this.fisika = fisika;
    }

    public int getBiologi() {
        return biologi;
    }

    public void setBiologi(int biologi) {
        this.biologi = biologi;
    }

    public int getKimia() {
        return kimia;
    }

    public void setKimia(int kimia) {
        this.kimia = kimia;
    }
}
